Suite *test_suite_calc(void) {
  Suite *s;
  TCase *tc_core;

  s = suite_create("S21 Test Calc");
  tc_core = tcase_create("Core");

  tcase_add_test(tc_core, s21_calc_complements_1);
  tcase_add_test(tc_core, s21_calc_complements_2);
  tcase_add_test(tc_core, s21_calc_complements_3);
  tcase_add_test(tc_core, s21_calc_complements_4);
  tcase_add_test(tc_core, s21_calc_complements_01);
  tcase_add_test(tc_core, s21_calc_complements_02);
  tcase_add_test(tc_core, s21_calc_complements_03);
  tcase_add_test(tc_core, s21_calc_complements_04);
  suite_add_tcase(s, tc_core);

  return s;
}
int test_calc() {
  int no_failed = 0;

  Suite *s;
  SRunner *runner;

  s = test_suite_calc();
  runner = srunner_create(s);
  srunner_set_fork_status(runner, CK_NOFORK);
  srunner_run_all(runner, CK_NORMAL);
  no_failed = srunner_ntests_failed(runner);
  srunner_free(runner);

  return no_failed;
}